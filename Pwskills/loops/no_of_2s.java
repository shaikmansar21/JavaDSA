import java.util.*;

public class no_of_2s {
    public static void main(String[] args) {
        int[]a={2,2,2,2,24,25,26,27,28,29,30};
        int count =0;


        for (int i = a[0]; i <= a[10]; i++) {


            if (i == 2)
                count++;



        }
        System.out.println(count);
    }
}
